package day04;

public class FahrenheitToCelcius {
    public static void main(String[] args) {

        // write a program that converts a Fahrenheit degree to Celsius degree

          Double celcius;
          Double fahrenheit = 28.0;

          celcius = (5.0/9) * (fahrenheit - 32);

         // System.out.println( celcius = (5.0/9) * (fahrenheit - 32));

          System.out.println(fahrenheit + "F equals to " + celcius + "C.");


    }
}
