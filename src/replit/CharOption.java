package replit;

public class CharOption {
    public static void main(String[] args) {
        char option = 'V';
        if (option == 'A'){
            System.out.println("You selected A ");
        }else if(option =='B'){
            System.out.println("You selected B ");
        }else if(option =='C'){
            System.out.println("You selected C ");
        }else {
            System.out.println("INVALID ENTRY");
        }
    }
}
